#include <bits/stdc++.h>
using namespace std;

int main() {
    int kwh;
    double diff;
    cin >> kwh;

    if(kwh >= 0 && kwh <= 50) {
        diff = 0;
    } else if (kwh >= 51 && kwh <= 100) {
        diff = (1.1 * kwh * 1728) - 1.1 * (50 * 1728 + (kwh - 50) * 1786);
    } else if (kwh >= 101 && kwh <= 200) {
        diff = 1.1 * (1728 * 100 + 2074 * (kwh - 100)) 
                - 1.1 * (50 * 1728 + 50 * 1786 + (kwh - 100) * 2074);
    } else if (kwh >= 201 && kwh <= 300) {
        diff = 1.1 * (1728 * 100 + 2074 * 100 + 2612 * (kwh - 200))
                - 1.1 * (50 * 1728 + 50 * 1786 + 100 * 2074 + (kwh - 200) * 2612);
    } else if (kwh >= 301 && kwh <= 400) {
        diff = 1.1 * (1728 * 100 + 2074 * 100 + 2612 * (kwh - 200))
                - 1.1 * (50 * 1728 + 50 * 1786 + 100 * 2074 + 100 * 2612 + (kwh - 300) * 2919);
    } else if (kwh >= 401 && kwh <= 700) {
        diff = 1.1 * (1728 * 100 + 2074 * 100 + 2612 * 200 + 3111 * (kwh - 400))
                - 1.1 * (50 * 1728 + 50 * 1786 + 100 * 2074 + 100 * 2612 + 100 * 2919 + (kwh - 400) * 3015);    
    } else {
        diff = 1.1 * (1728 * 100 + 2074 * 100 + 2612 * 200 + 3111 * 300 + 3457 * (kwh - 700))
                - 1.1 * (50 * 1728 + 50 * 1786 + 100 * 2074 + 100 * 2612 + 100 * 2919 + 3015 * (kwh - 400));
    }

    cout << fixed << setprecision(2) << diff << endl;
}